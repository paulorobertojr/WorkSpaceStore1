    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (JOptionPane.showConfirmDialog(rootPane, "Confirmar a exclusão do livro selecionado?") == 0); {
        ModelLivros ml = new ModelLivros();
        try {
            ml.remove((Long) tbLivros.getValueAt(tbLivros.getSelectedRow(), 0));
            JOptionPane.showMessageDialog(rootPane, "Livro selecionado excluído!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao excluir livro: "+e.getMessage());
        }
    }//GEN-LAST:event_btExcluirActionPerformed
}
