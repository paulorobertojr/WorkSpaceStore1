    private void montarTabela(){
        tabela = (DefaultTableModel) tbLivros.getModel();
        ModelLivros ml = new ModelLivros();
        for (Livros l : ml.listaLivros()) {
            tabela.addRow(new Object[]{l.getId(),l.getTitulo(),l.getAutor(),l.getEdicao(),l.getGenero(),l.getEditora(),l.getPais(),l.getIsbn(),l.getPreco(),l.getPreco()});
        }
    }

