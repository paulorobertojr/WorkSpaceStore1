    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (JOptionPane.showConfirmDialog(rootPane, "Confirmar a excclusão do funcionário selecionado?") == 0); {
        ModelFuncionario mf = new ModelFuncionario();
        try {
            mf.remove((Long) tbFuncionario.getValueAt(tbFuncionario.getSelectedRow(), 0));
            JOptionPane.showMessageDialog(rootPane, "Funcionário selecionado excluído!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao excluir funcionário: "+e.getMessage());
        }
    }     
    }//GEN-LAST:event_btExcluirActionPerformed

