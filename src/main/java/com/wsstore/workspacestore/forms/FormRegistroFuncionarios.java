     private void montarTabela(){
        tabela = (DefaultTableModel) tbFuncionario.getModel();
         ModelFuncionario mf = new ModelFuncionario();
        for (Funcionario f : mf.listaFuncionario()) {
            tabela.addRow(new Object[]{f.getId(),f.getNomeFunc(),f.getEmailFunc(),f.getTelefoneFunc(),f.getCpfFunc(),f.getIdadeFunc(),f.getFuncao(),f.getSenha()});
        }
    }
