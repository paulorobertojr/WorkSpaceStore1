    private void montarTabela(){
        tabela = (DefaultTableModel) tbClientes.getModel();
        ModelCliente mc = new ModelCliente();
        for (Cliente c : mc.listaCliente()) {
            tabela.addRow(new Object[]{c.getId(),c.getNome(),c.getCpf(),c.getEmail(),c.getTelefone(),c.getIdade()});
        }
    }

