    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (JOptionPane.showConfirmDialog(rootPane, "Confirmar a exclusão do cliente selecionado?") == 0); {
        ModelCliente mc = new ModelCliente();
        try {
            mc.remove((Long) tbClientes.getValueAt(tbClientes.getSelectedRow(), 0));
            JOptionPane.showMessageDialog(rootPane, "Cliente selecionado excluído!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao excluir cliente: "+e.getMessage());
        }
    }     
    }//GEN-LAST:event_btExcluirActionPerformed

